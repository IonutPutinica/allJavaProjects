
public class multiplication
{
   public static void main(String args[])
   {
      int noApples = 6;
      double priceApples = 2.75;
      double totalPrice;
      totalPrice = noApples * priceApples;
      System.out.println(
            "I have " + noApples + " apples, the price per apple being "
                  + priceApples + " so my total net worth is " + totalPrice);
   }
}
